public class Paciente {
    public String nombre;
    public int edad;
    public String apellido;
    public int dni;
    public String prepaga;
    public Paciente(String _nombre, int _edad, String _apellido, int _dni, String _prepaga){
        nombre = _nombre;
        edad = _edad;
        apellido = _apellido;
        dni = _dni;
        prepaga = _prepaga;

    }
}
